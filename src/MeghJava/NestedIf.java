package MeghJava;

public class NestedIf {

	public static void main(String[] args) {
		 int a = 200 ;
		 int b = 900 ;
		 int c = 500 ;
		 
		 if (a>b && a>c) {
			 System.out.println(" A is the greatest " +a);
		 } else if (b>c) {
			 System.out.println(" B is the greatest " +b);
		 } 
		 else 
		 {
			 System.out.println(" C is the greatest " +c);
	}

	}
}
