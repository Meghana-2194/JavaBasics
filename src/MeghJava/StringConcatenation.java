package MeghJava;

public class StringConcatenation {

	public static void main(String[] args) {
		// Concatenation : '+'
		int a = 100 ;
		int b = 200 ;
		 String x = "Hello" ;
		 String y = "okay" ;
		 
		 System.out.println(a+b+x+y); 
		 System.out.println(x+y+a+b);
		 System.out.println(x+y+(a+b));
		 System.out.println("Print the int :"  +(a+b));
		 System.out.println("Print the int :"  +b);

	}

}
